<template>
  <BkLoading
    class="pulsar-cluster-expansion-box"
    :loading="isLoading">
    <BkAlert
      class="mb16"
      theme="warning"
      :title="t('Bookkeeper，Broker 至少扩容一种类型')" />
    <BkRadioGroup
      v-model="ipSource"
      class="ip-srouce-box">
      <BkRadioButton label="resource_pool">
        {{ t('资源池自动匹配') }}
      </BkRadioButton>
      <BkRadioButton label="manual_input">
        {{ t('手动选择') }}
      </BkRadioButton>
    </BkRadioGroup>
    <div class="wrapper">
      <NodeStatusList
        ref="nodeStatusListRef"
        v-model="nodeType"
        :ip-source="ipSource"
        :list="nodeStatusList"
        :node-info="nodeInfoMap" />
      <div class="node-panel">
        <HostExpansion
          v-if="!isLoading"
          :key="nodeType"
          v-model:expansion-disk="nodeInfoMap[nodeType].expansionDisk"
          v-model:host-list="nodeInfoMap[nodeType].hostList"
          v-model:resource-spec="nodeInfoMap[nodeType].resourceSpec"
          :cloud-info="{
            id: data.bk_cloud_id,
            name: data.bk_cloud_name,
          }"
          :data="nodeInfoMap[nodeType]"
          :db-type="DBTypes.PULSAR"
          :disable-host-method="disableHostMethod"
          :ip-source="ipSource" />
      </div>
    </div>
  </BkLoading>
</template>
<script setup lang="tsx">
  import { InfoBox } from 'bkui-vue';
  import { reactive, ref } from 'vue';
  import { useI18n } from 'vue-i18n';

  import PulsarModel from '@services/model/pulsar/pulsar';
  import PulsarMachineModel from '@services/model/pulsar/pulsar-machine';
  import { getPulsarMachineList } from '@services/source/pulsar';
  import { createTicket } from '@services/source/ticket';

  import { useTicketMessage } from '@hooks';

  import { ClusterTypes, DBTypes, TicketTypes } from '@common/const';

  import HostExpansion, { type TExpansionNode } from '@views/db-manage/common/host-expansion/Index.vue';
  import NodeStatusList from '@views/db-manage/common/host-expansion/NodeStatusList.vue';

  import { messageError } from '@utils';

  interface Props {
    data: PulsarModel;
  }

  type Emits = (e: 'change') => void;

  interface Exposes {
    submit: () => Promise<any>;
  }

  const props = defineProps<Props>();
  const emits = defineEmits<Emits>();

  const makeMapByHostId = (hostList: TExpansionNode['hostList'] = []) =>
    hostList.reduce(
      (result, item) => ({
        ...result,
        [item.bk_host_id]: true,
      }),
      {} as Record<number, boolean>,
    );

  const { t } = useI18n();
  const ticketMessage = useTicketMessage();

  const nodeStatusList = [
    {
      key: 'broker',
      label: 'Broker',
    },
    {
      key: 'bookkeeper',
      label: 'Bookkeeper',
    },
  ];

  const nodeInfoMap = reactive<Record<string, TExpansionNode>>({
    bookkeeper: {
      clusterId: props.data.id,
      // targetDisk: 0,
      expansionDisk: 0,
      hostList: [],
      ipSource: 'resource_pool',
      label: 'Bookkeeper',
      originalHostList: [],
      resourceSpec: {
        count: 0,
        spec_id: 0,
      },
      role: 'pulsar_bookkeeper',
      specClusterType: ClusterTypes.PULSAR,
      specMachineType: 'pulsar_bookkeeper',
      tagText: t('存储层'),
      totalDisk: 0,
    },
    broker: {
      clusterId: props.data.id,
      // targetDisk: 0,
      expansionDisk: 0,
      hostList: [],
      ipSource: 'resource_pool',
      label: 'Broker',
      originalHostList: [],
      resourceSpec: {
        count: 0,
        spec_id: 0,
      },
      role: 'pulsar_broker',
      specClusterType: ClusterTypes.PULSAR,
      specMachineType: 'pulsar_broker',
      tagText: t('接入层'),
      totalDisk: 0,
    },
  });

  const nodeStatusListRef = ref();
  const isLoading = ref(false);
  const ipSource = ref('resource_pool');
  const nodeType = ref('broker');

  // 获取主机详情
  const fetchHostDetail = () => {
    isLoading.value = true;
    getPulsarMachineList({
      cluster_ids: String(props.data.id),
      limit: -1,
      offset: 0,
    })
      .then((data) => {
        const bookkeeperOriginalHostList: PulsarMachineModel[] = [];
        const brokerOriginalHostList: PulsarMachineModel[] = [];

        let bookkeeperDiskTotal = 0;
        let brokerDiskTotal = 0;

        data.results.forEach((hostItem) => {
          if (hostItem.isBookKeeper) {
            bookkeeperDiskTotal += Math.floor(Number(hostItem.host_info.bk_disk));
            bookkeeperOriginalHostList.push(hostItem);
          } else if (hostItem.isBroker) {
            brokerDiskTotal += Math.floor(Number(hostItem.host_info.bk_disk));
            brokerOriginalHostList.push(hostItem);
          }
        });

        nodeInfoMap.bookkeeper.totalDisk = bookkeeperDiskTotal;
        nodeInfoMap.bookkeeper.originalHostList = bookkeeperOriginalHostList;

        nodeInfoMap.broker.totalDisk = brokerDiskTotal;
        nodeInfoMap.broker.originalHostList = brokerOriginalHostList;
      })
      .finally(() => {
        isLoading.value = false;
      });
  };

  fetchHostDetail();

  // 扩容主机节点互斥
  const disableHostMethod = (hostData: TExpansionNode['hostList'][0]) => {
    const bookkeeperDisableHostMethod = (hostData: TExpansionNode['hostList'][0]) => {
      const brokerHostIdMap = makeMapByHostId(nodeInfoMap.broker.hostList);
      if (brokerHostIdMap[hostData.bk_host_id]) {
        return t('主机已被xx节点使用', ['Broker']);
      }
      return false;
    };
    const brokerDisableHostMethod = (hostData: TExpansionNode['hostList'][0]) => {
      const bookkeeperHostIdMap = makeMapByHostId(nodeInfoMap.bookkeeper.hostList);
      if (bookkeeperHostIdMap[hostData.bk_host_id]) {
        return t('主机已被xx节点使用', ['Bookkeeper']);
      }
      return false;
    };

    if (nodeType.value === 'bookkeeper') {
      return bookkeeperDisableHostMethod(hostData);
    }
    if (nodeType.value === 'broker') {
      return brokerDisableHostMethod(hostData);
    }

    return false;
  };

  defineExpose<Exposes>({
    submit() {
      if (!nodeStatusListRef.value.validate()) {
        messageError(t('Bookkeeper_Broker 至少扩容一种类型'));
        return Promise.reject();
      }

      const renderSubTitle = () => {
        const renderExpansionDiskTips = () =>
          Object.values(nodeInfoMap).map((nodeData) => {
            if (nodeData.expansionDisk) {
              return (
                <div>
                  {t('name容量从nG扩容至nG', {
                    expansionDisk: nodeData.totalDisk + nodeData.expansionDisk,
                    name: nodeData.label,
                    totalDisk: nodeData.totalDisk,
                  })}
                </div>
              );
            }
            return null;
          });

        return <div style='font-size: 14px; line-height: 28px; color: #63656E;'>{renderExpansionDiskTips()}</div>;
      };

      return new Promise((resolve, reject) => {
        InfoBox({
          cancelText: t('取消'),
          confirmText: t('确认'),
          contentAlign: 'center',
          footerAlign: 'center',
          headerAlign: 'center',
          onCancel: () => reject(),
          onConfirm: () => {
            const hostData = {};

            const generateExtInfo = () =>
              Object.entries(nodeInfoMap).reduce(
                (results, [key, item]) => {
                  Object.assign(results, {
                    [key]: {
                      expansion_disk: item.expansionDisk,
                      total_disk: item.totalDisk,
                      total_hosts: item.originalHostList.length,
                    },
                  });
                  return results;
                },
                {} as Record<string, any>,
              );

            if (ipSource.value === 'manual_input') {
              const formatHost = (hostList: TExpansionNode['hostList'] = []) => {
                const hosts = hostList.map((hostItem) => ({
                  bk_biz_id: hostItem.dedicated_biz,
                  bk_cloud_id: hostItem.bk_cloud_id,
                  bk_disk: hostItem.bk_disk,
                  bk_host_id: hostItem.bk_host_id,
                  ip: hostItem.ip,
                }));
                return {
                  count: hostList.length,
                  hosts,
                  spec_id: 0,
                };
              };
              Object.assign(hostData, {
                resource_spec: {
                  bookkeeper: formatHost(nodeInfoMap.bookkeeper.hostList),
                  broker: formatHost(nodeInfoMap.broker.hostList),
                },
              });
            } else {
              const resourceSpec = {};
              if (nodeInfoMap.broker.resourceSpec.spec_id > 0 && nodeInfoMap.broker.resourceSpec.count > 0) {
                Object.assign(resourceSpec, {
                  broker: nodeInfoMap.broker.resourceSpec,
                });
              }
              if (nodeInfoMap.bookkeeper.resourceSpec.spec_id > 0 && nodeInfoMap.bookkeeper.resourceSpec.count > 0) {
                Object.assign(resourceSpec, {
                  bookkeeper: nodeInfoMap.bookkeeper.resourceSpec,
                });
              }
              Object.assign(hostData, {
                resource_spec: resourceSpec,
              });
            }

            createTicket({
              bk_biz_id: window.PROJECT_CONFIG.BIZ_ID,
              details: {
                cluster_id: props.data.id,
                ext_info: generateExtInfo(),
                ip_source: 'resource_pool',
                ...hostData,
              },
              ticket_type: TicketTypes.PULSAR_SCALE_UP,
            }).then((data) => {
              ticketMessage(data.id);
              resolve('success');
              emits('change');
            });
          },
          subTitle: renderSubTitle,
          title: t('确认扩容【name】集群', { name: props.data.cluster_name }),
        });
      });
    },
  });
</script>
