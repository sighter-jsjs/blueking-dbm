<template>
  <div class="instance-selector-manual-input">
    <BkResizeLayout
      :border="false"
      collapsible
      initial-divide="480px"
      :max="600"
      :min="420">
      <template #aside>
        <div class="manual-input-wrapper">
          <BkInput
            ref="inputRef"
            v-model.trim="inputState.values"
            class="manual-input-textarea"
            :placeholder="inputState.placeholder"
            type="textarea"
            @input="handleInput" />
          <div class="manual-input-errors">
            <span
              v-if="errorState.format.show"
              class="mr-8">
              <I18nT
                keypath="n处格式错误"
                tag="span">
                <strong>{{ errorState.format.count }}</strong>
              </I18nT>
              <DbIcon
                v-bk-tooltips="$t('标记错误')"
                class="manual-input-icons"
                type="audit"
                @click="handleSelectionError('format')" />
            </span>
            <span v-if="errorState.instance.show">
              <I18nT
                keypath="n处IP_Port不存在"
                tag="span">
                <strong>{{ errorState.instance.count }}</strong>
              </I18nT>
              <DbIcon
                v-bk-tooltips="$t('标记错误')"
                class="manual-input-icons"
                type="audit"
                @click="handleSelectionError('instance')" />
            </span>
          </div>
          <div class="manual-input-buttons">
            <BkButton
              class="mr-8"
              :disabled="!inputState.values"
              :loading="inputState.isLoading"
              outline
              size="small"
              theme="primary"
              @click="handleParsingValues">
              {{ $t('解析并添加') }}
            </BkButton>
            <BkButton
              class="w-88"
              size="small"
              @click="() => (inputState.values = '')">
              {{ $t('清空') }}
            </BkButton>
          </div>
        </div>
      </template>
      <template #main>
        <BkLoading :loading="inputState.isLoading">
          <RenderManualHost
            :last-values="lastValues"
            :role="role"
            :table-data="inputState.tableData"
            :table-settings="tableSettings"
            @change="handleHostChange" />
        </BkLoading>
      </template>
    </BkResizeLayout>
  </div>
</template>
<script setup lang="ts">
  import type { Table } from 'bkui-vue';
  import _ from 'lodash';
  import { useI18n } from 'vue-i18n';

  import RedisClusterNodeByIpModel from '@services/model/redis/redis-cluster-node-by-ip';
  import { checkRedisInstances } from '@services/source/instances';
  import { queryMasterSlavePairs } from '@services/source/redisToolbox';
  import type { InstanceInfos } from '@services/types';

  import { useGlobalBizs } from '@stores';

  import { ipv4 } from '@common/regex';

  import type { InstanceSelectorValues } from '../Index.vue';

  import RenderManualHost from './RenderManualHost.vue';

  export interface InstanceItem extends Omit<InstanceInfos, 'spec_config'> {
    spec_config: RedisClusterNodeByIpModel['spec_config'];
  }

  interface Props {
    lastValues: InstanceSelectorValues;
    role?: string;
    tableSettings: InstanceType<typeof Table>['$props']['settings'];
  }

  type Emits = (e: 'change', value: InstanceSelectorValues) => void;
  const props = defineProps<Props>();
  const emits = defineEmits<Emits>();

  const { t } = useI18n();
  const { currentBizId } = useGlobalBizs();
  const inputRef = ref();

  const inputState = reactive({
    isLoading: false,
    placeholder: t('请输入IP如_1_1_1_1多个可使用换行_空格或_分隔'),
    tableData: [] as InstanceItem[],
    values: '',
  });
  const errorState = reactive({
    format: {
      count: 0,
      selectionEnd: 0,
      selectionStart: 0,
      show: false,
    },
    instance: {
      count: 0,
      selectionEnd: 0,
      selectionStart: 0,
      show: false,
    },
  });

  const handleHostChange = (values: InstanceSelectorValues) => {
    emits('change', values);
  };

  const handleInput = () => {
    errorState.format.show = false;
    errorState.instance.show = false;
  };

  /**
   * 标记错误
   */
  const handleSelectionError = (key: 'format' | 'instance') => {
    const { selectionEnd, selectionStart } = errorState[key];
    const textarea = inputRef.value?.$el?.getElementsByTagName?.('textarea')?.[0];
    if (textarea) {
      (textarea as HTMLInputElement).focus();
      (textarea as HTMLInputElement).setSelectionRange(selectionStart, selectionEnd);
    }
  };

  /**
   * 处理分隔内容，过滤空内容
   */
  const getValues = () =>
    inputState.values
      .replace(/\s+|[;,|]/g, ' ') // 将空格 换行符 ；，｜符号统一为空格
      .split(' ')
      .filter((value) => value);

  /**
   * 解析输入内容
   */
  const handleParsingValues = async () => {
    const formatErrorLines: string[] = [];
    const lines = getValues();
    const availableLines: string[] = [];
    // 处理格式错误
    lines.forEach((line) => {
      if (!ipv4.test(line)) {
        formatErrorLines.push(line);
      } else {
        availableLines.push(line);
      }
    });
    const count = formatErrorLines.length;
    errorState.format.count = count;
    errorState.format.selectionStart = 0;
    errorState.format.selectionEnd = formatErrorLines.join('\n').length;
    inputState.isLoading = true;
    // 检查 IP 是否存在
    const queryResult = await checkRedisInstances({
      bizId: currentBizId,
      instance_addresses: availableLines,
    });
    // 获取ip主从关系
    const masterSlaveResult = await Promise.all(
      queryResult.map((item) => queryMasterSlavePairs({ cluster_id: item.cluster_id })),
    );
    const masterSlaveMap = masterSlaveResult.flat().reduce<Record<string, string>>((acc, cur) => {
      Object.assign(acc, { [cur.master_ip]: cur.slave_ip });
      return acc;
    }, {});
    const ipsSet = new Set(availableLines);
    const legalInstances = _.uniqBy(
      queryResult.reduce<InstanceItem[]>((acc, cur) => {
        if (ipsSet.has(cur.ip)) {
          // 格式化实例角色
          const roleMap = {
            master: 'redis_master',
            proxy: 'proxy',
            slave: 'redis_slave',
          };
          acc.push({
            ...cur,
            role: roleMap[cur.role as keyof typeof roleMap],
          });
          // 如果是主ip则带出从ip
          if (cur.role === 'master') {
            const slaveIp = masterSlaveMap[cur.ip];
            acc.push({
              ...cur,
              ip: slaveIp,
              role: 'redis_slave',
            });
          }
          ipsSet.delete(cur.ip);
        }
        return acc;
      }, []),
      'ip',
    );
    inputState.isLoading = false;
    // 错误ip(未找到)
    const checkErrorLines = [...ipsSet];

    inputState.tableData.splice(0, inputState.tableData.length, ...legalInstances);
    errorState.instance.count = checkErrorLines.length;
    const { selectionEnd } = errorState.format;
    errorState.instance.selectionStart = selectionEnd === 0 ? 0 : selectionEnd + 1;
    errorState.instance.selectionEnd = checkErrorLines.join('\n').length + errorState.format.selectionEnd + 1;

    const formatValue = (data: InstanceItem) => ({
      bk_biz_id: window.PROJECT_CONFIG.BIZ_ID,
      bk_cloud_id: data?.host_info?.cloud_id || 0,
      bk_host_id: data?.bk_host_id || 0,
      cluster_domain: data.related_clusters[0].master_domain ?? '',
      cluster_ids: data.related_clusters.map((item) => item.id) || [],
      ip: data?.ip || '',
      role: data?.role || '',
      spec_config: data?.spec_config || null,
    });

    // 解析完成后选中
    const lastValues = { ...props.lastValues };
    for (const item of inputState.tableData) {
      const list = lastValues.idleHosts;
      const isExisted = list.find((i) => i.ip === item.ip);
      if (!isExisted) {
        item.cluster_domain = item.master_domain;
        lastValues.idleHosts.push(formatValue(item));
      }
    }
    emits('change', {
      ...props.lastValues,
      ...lastValues,
    });
    errorState.format.show = count > 0;
    errorState.instance.show = checkErrorLines.length > 0;
    inputState.isLoading = false;

    const newLines = [...formatErrorLines, ...checkErrorLines];
    // 将调整好的内容回填显示
    inputState.values = newLines.join('\n');
  };
</script>

