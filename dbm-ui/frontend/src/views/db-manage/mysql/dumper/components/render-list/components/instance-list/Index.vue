        class="inline-block">
        <BkButton
          :disabled="!hasSelected"
          @click="handleBatchStopInstance">
          {{ t('禁用') }}
        </BkButton>
      </span>
      <span
        v-bk-tooltips="{
          content: t('请选择实例'),
          disabled: hasSelected,
        }"
        class="inline-block">
        <BkButton
          :disabled="!hasSelected"
          @click="handleBatchDeleteInstance">
          {{ t('删除') }}
        </BkButton>
      </span>
      <span
        v-bk-tooltips="{
          content: t('请选择实例'),
          disabled: hasSelected,
        }"
        class="inline-block">
        <BkDropdown :disabled="!hasSelected">
          <BkButton
            class="dropdown-button"
            :disabled="!hasSelected">
            {{ t('复制') }}
            <DbIcon type="up-big dropdown-button-icon" />
          </BkButton>
          <template #content>
            <BkDropdownMenu>
              <BkDropdownItem @click="handleCopyAll()">
                {{ t('所有IP') }}
              </BkDropdownItem>
              <BkDropdownItem @click="handleCopyAll(true)">
                {{ t('所有实例') }}
              </BkDropdownItem>
              <BkDropdownItem @click="handleCopySelected()">
                {{ t('已选IP') }}
              </BkDropdownItem>
              <BkDropdownItem @click="handleCopySelected(true)">
                {{ t('已选实例') }}
              </BkDropdownItem>
            </BkDropdownMenu>
          </template>
        </BkDropdown>
      </span>
      <div class="instances-view-operations-right">
        <DbSearchSelect
          v-model="search"
          clearable
          :data="searchSelectData"
          :placeholder="t('请选择条件搜索')"
          style="width: 500px"
          :validate-values="validateValues"
          @change="fetchTableData" />
      </div>
    </div>
    <BkAlert
      v-if="tableDataCount === 0 && runningTicketList.length > 0"
      style="margin-bottom: 16px"
      theme="warning">
      <template #title>
        <div>
          {{ t('已有n个订阅单据正在进行中', { n: runningTicketList.length }) }}
          <BkButton
            v-for="item in runningTicketList"
            :key="item"
            class="mr-8"
            text
            theme="primary"
            @click="() => handleGoTicket(item)">
            {{ item }}
          </BkButton>
        </div>
      </template>
    </BkAlert>
    <DbTable
      ref="tableRef"
      class="table-box mb-24"
      :columns="columns"
      :data-source="listDumperInstance"
      primary-key="dumper_id"
      :row-class="setRowClass"
      :row-config="{
        useKey: true,
        keyField: 'id',
      }"
      selectable
      :settings="settings"
      show-settings
      @clear-search="handleClearFilters"
      @column-filter="handleColumnFilter"
      @request-finished="handleTableRequestFinished"
      @selection="handleSelect" />
  </div>
  <AppendSubscribeSlider
    v-model="showAppendSubscribeSlider"
    :data="data" />
  <ManualMigration
    v-if="activeRow"
    v-model="showManualMigration"
    :data="activeRow"
    @success="fetchTableData" />
</template>

<script setup lang="tsx">
  import { InfoBox } from 'bkui-vue';
  import type { ISearchValue } from 'bkui-vue/lib/search-select/utils';
  import { useI18n } from 'vue-i18n';
  import { useRequest } from 'vue-request';

  import DumperInstanceModel from '@services/model/dumper/dumper';
  import { getRunningTaskList, listDumperConfig, listDumperInstance } from '@services/source/dumper';
  import { createTicket } from '@services/source/ticket';

  import { useTicketMessage } from '@hooks';

  import { useGlobalBizs } from '@stores';

  import { TicketTypes } from '@common/const';
  import { ipPort, ipv4 } from '@common/regex';

  import MiniTag from '@components/mini-tag/index.vue';
  import TextOverflowLayout from '@components/text-overflow-layout/Index.vue';

  import RenderOperationTagNew from '@views/db-manage/common/RenderOperationTagNew.vue';

  import { execCopy, getSearchSelectorParams } from '@utils';

  import AppendSubscribeSlider from '../append-subscribe/Index.vue';

  import ManualMigration from './manual-migration/Index.vue';
  import OperationBtnTip from './OperationBtnTip.vue';

  export type DumperConfig = ServiceReturnType<typeof listDumperConfig>['results'][number];

  interface Props {
    data: DumperConfig | null;
  }

  const props = defineProps<Props>();

  const ticketMessage = useTicketMessage();
  const { currentBizId } = useGlobalBizs();
  const { locale, t } = useI18n();
  const router = useRouter();

  const searchSelectData = [
    {
      id: 'address',
      name: t('实例'),
    },
    {
      id: 'ip',
      name: 'IP',
    },
    {
      id: 'dumper_id',
      name: t('实例ID'),
    },
    {
      id: 'source_cluster',
      name: t('数据源集群'),
    },
    {
      children: [
        {
          id: 'KAFKA',
          name: 'KAFKA',
        },
        {
          id: 'L5_AGENT',
          name: 'L5_AGENT',
        },
        {
          id: 'TCP/IP',
          name: 'TCP/IP',
        },
      ],
      id: 'protocol_type',
      multiple: true,
      name: t('接收端类型'),
    },
    {
      id: 'target_address',
      name: t('接收端地址'),
    },
    {
      children: [
        {
          id: 'full_sync',
          name: t('全量同步'),
        },
        {
          id: 'incr_sync',
          name: t('增量同步'),
        },
      ],
      id: 'add_type',
      multiple: true,
      name: t('同步方式'),
    },
  ];

  const tableRef = ref();
  const search = ref<ISearchValue[]>([]);
  const tableDataCount = ref(0);
  const showAppendSubscribeSlider = ref(false);
  const showManualMigration = ref(false);
  const activeRow = ref<DumperInstanceModel>();
  const runningTicketList = ref<number[]>([]);

  const selectedList = shallowRef<DumperInstanceModel[]>([]);

  const hasSelected = computed(() => selectedList.value.length > 0);

  const isCN = computed(() => locale.value === 'zh-cn');

  const syncTypeMap = {
    full_sync: t('全量同步'),
    incr_sync: t('增量同步'),
  } as Record<string, string>;

  const columns = [
    {
      field: 'instance',
      fixed: 'left',
      label: t('实例'),
      minWidth: 200,
      render: ({ data }: { data: DumperInstanceModel }) => (
        <TextOverflowLayout>
          {{
            append: () => (
              <>
                {data.need_transfer && (
                  <bk-popover
                    placement='top'
                    popover-delay={[100, 200]}
                    theme='light'>
                    {{
                      content: () => <div>{t('Dumper实例迁移失败')}</div>,
                      default: () => (
                        <db-icon
                          class='migrate-fail-tip'
                          type='exclamation-fill'
                        />
                      ),
                    }}
                  </bk-popover>
                )}
                <RenderOperationTagNew data={data.operationTagTip} />
                {!data.isOnline && !data.isStarting && (
                  <MiniTag
                    content={t('已禁用')}
                    extCls='stoped-icon'
                  />
                )}
                {data.isNew && (
                  <MiniTag
                    content='NEW'
                    extCls='success-icon'
                    theme='success'
                  />
                )}
              </>
            ),
            default: () => <span class='mr-4'>{`${data.ip}:${data.listen_port}`}</span>,
          }}
        </TextOverflowLayout>
      ),
      showOverflowTooltip: false,
      width: 230,
    },
    {
      field: 'dumper_id',
      label: t('实例 ID'),
      width: 80,
    },
    {
      field: 'source_cluster',
      label: t('数据源集群'),
      minWidth: 200,
      render: ({ data }: { data: DumperInstanceModel }) =>
        data.source_cluster ? (
          <auth-router-link
            to={{
              name: 'DatabaseTendbha',
              query: {
                id: data.id,
              },
            }}
            action-id='mysql_view'
            permission={data.permission.mysql_view}
            resource={data.source_cluster.id}
            target='_blank'>
            {data.source_cluster.immute_domain}:{data.source_cluster.master_port}
          </auth-router-link>
        ) : (
          '--'
        ),
      width: 250,
    },
    {
      field: 'protocol_type',
      filter: {
        list: [
          { text: 'KAFKA', value: 'KAFKA' },
          { text: 'L5_AGENT', value: 'L5_AGENT' },
          { text: 'TCP/IP', value: 'TCP/IP' },
        ],
      },
      label: t('接收端类型'),
    },
    {
      field: 'receiver',
      label: t('接收端地址'),
      render: ({ data }: { data: DumperInstanceModel }) => (
        <span>
          {data.target_address}:{data.target_port}
        </span>
      ),
    },
    {
      field: 'add_type',
      filter: {
        list: [
          { text: t('全量同步'), value: 'full_sync' },
          { text: t('增量同步'), value: 'incr_sync' },
        ],
      },
      label: t('同步方式'),
      render: ({ data }: { data: DumperInstanceModel }) => <span>{syncTypeMap[data.add_type]}</span>,
    },
    {
      field: '',
      fixed: 'right',
      label: t('操作'),
      render: ({ data }: { data: DumperInstanceModel }) => (
        <>
          <OperationBtnTip
            data={data}
            disabled={!data.isOperating}>
            <span>
              <auth-button
                action-id='tbinlogdumper_enable_disable'
                class='mr-8'
                disabled={data.isOperating}
                permission={data.permission.tbinlogdumper_enable_disable}
                resource={data.cluster_id}
                theme='primary'
                text
                onClick={() => handleOpenOrCloseInstance(data)}>
                {data.isOnline ? t('禁用') : t('启用')}
              </auth-button>
            </span>
          </OperationBtnTip>
          {!data.isOnline && (
            <OperationBtnTip
              data={data}
              disabled={!data.isOperating}>
              <span>
                <auth-button
                  action-id='tbinlogdumper_reduce_nodes'
                  class='mr-8'
                  disabled={data.isOperating}
                  permission={data.permission.tbinlogdumper_reduce_nodes}
                  resource={data.cluster_id}
                  theme='primary'
                  text
                  onClick={() => handleDeleteInstance(data)}>
                  {t('删除')}
                </auth-button>
              </span>
            </OperationBtnTip>
          )}
          {data.need_transfer && data.source_cluster && (
            <OperationBtnTip
              data={data}
              disabled={!data.isOperating}>
              <span>
                <auth-button
                  action-id='tbinlogdumper_switch_nodes'
                  disabled={data.isOperating}
                  permission={data.permission.tbinlogdumper_switch_nodes}
                  resource={data.cluster_id}
                  theme='primary'
                  text
                  onClick={() => handleOpenManualMigration(data)}>
                  {t('手动迁移')}
                </auth-button>
              </span>
            </OperationBtnTip>
          )}
        </>
      ),
      width: isCN.value ? 160 : 220,
    },
  ];

  const settings = {
    checked: ['instance', 'dumper_id', 'source_cluster', 'protocol_type', 'receiver', 'add_type'],
    fields: [
      {
        disabled: true,
        field: 'instance',
        label: t('实例'),
      },
      {
        field: 'dumper_id',
        label: t('实例 ID'),
      },
      {
        field: 'source_cluster',
        label: t('数据源集群'),
      },
      {
        field: 'protocol_type',
        label: t('接收端类型'),
      },
      {
        field: 'receiver',
        label: t('接收端地址'),
      },
      {
        field: 'add_type',
        label: t('同步方式'),
      },
    ],
  };

  const { run: fetchRunningTaskList } = useRequest(getRunningTaskList, {
    manual: true,
    onSuccess(result) {
      runningTicketList.value = result;
    },
  });

  const { run: runCreateTicket } = useRequest(createTicket, {
    manual: true,
    onSuccess: (data) => {
      if (data && data.id) {
        ticketMessage(data.id);
        fetchTableData();
      }
    },
  });

  const fetchTableData = () => {
    const searchParams = getSearchSelectorParams(search.value);
    tableRef.value?.fetchData(searchParams, {
      config_name: props.data === null ? undefined : props.data.name,
    });
  };

  watch(
    () => [props.data, search],
    () => {
      fetchTableData();
      tableRef.value?.clearSelected();
      if (props.data) {
        fetchRunningTaskList({
          dumper_config_id: props.data?.id,
        });
      }
    },
    {
      immediate: true,
    },
  );

  // tip: async 去掉组件库会报错
  const validateValues = async (item: { id: string }, values: ISearchValue['values']) => {
    if (values) {
      const targetValue = values[0].id.replace(/^\s+|\s+$/g, '');
      if (item.id === 'address') {
        const list = targetValue.split(',');
        if (list.some((item) => !ipPort.test(item))) {
          return t('格式错误');
        }
      }
      if (item.id === 'ip' && !ipv4.test(targetValue)) {
        return t('格式错误');
      }
      return true;
    }
    return false;
  };

  const handleGoTicket = (ticketId?: number) => {
    if (!ticketId) {
      return;
    }
    const route = router.resolve({
      name: 'bizTicketManage',
      params: {
        ticketId,
      },
    });
    window.open(route.href);
  };

  const handleAppendInstance = () => {
    showAppendSubscribeSlider.value = true;
  };

  const handleOpenOrCloseInstance = (data: DumperInstanceModel) => {
    if (data.isOperating) {
      return;
    }
    if (data.isOnline) {
      InfoBox({
        confirmText: t('禁用'),
        content: (
          <div>
            <div>
              {t('实例')}：{data.ip}:{data.listen_port}
            </div>
            <div style='margin-top: 8px;'>{t('禁用后数据传输将会终止，请谨慎操作！')}</div>
          </div>
        ),
        infoType: 'warning',
        onConfirm: () => {
          const params = {
            bk_biz_id: currentBizId,
            details: {
              dumper_instance_ids: [data.id],
            },
            remark: '',
            ticket_type: TicketTypes.TBINLOGDUMPER_DISABLE_NODES,
          };
          runCreateTicket(params);
        },
        title: t('确认禁用该实例？'),
        width: 400,
      });
      return;
    }
    // 启用
    const params = {
      bk_biz_id: currentBizId,
      details: {
        dumper_instance_ids: [data.id],
      },
      remark: '',
      ticket_type: TicketTypes.TBINLOGDUMPER_ENABLE_NODES,
    };
    runCreateTicket(params);
  };

  // 批量禁用
  const handleBatchStopInstance = () => {
    InfoBox({
      confirmText: t('禁用'),
      extCls: 'dumper-instance-infobox',
      infoType: 'warning',
      onConfirm: () => {
        const params = {
          bk_biz_id: currentBizId,
          details: {
            dumper_instance_ids: selectedList.value.map((item) => item.id),
          },
          remark: '',
          ticket_type: TicketTypes.TBINLOGDUMPER_DISABLE_NODES,
        };
        runCreateTicket(params);
      },
      subTitle: t('禁用后数据传输将会终止，请谨慎操作！'),
      title: t('确认批量禁用n个实例？', { n: selectedList.value.length }),
      width: 400,
    });
  };

  // 删除
  const handleDeleteInstance = (data: DumperInstanceModel) => {
    if (data.isOperating) {
      return;
    }
    InfoBox({
      confirmButtonTheme: 'danger',
      confirmText: t('删除'),
      content: (
        <div class='dumper-instance-infobox-subtitle'>
          <div>
            {t('实例')}：{data.ip}:{data.listen_port}
          </div>
          <div style='margin-top: 8px;'>{t('删除后数据传输将会终止，并删除实例，请谨慎操作！')}</div>
        </div>
      ),
      onConfirm: () => {
        const params = {
          bk_biz_id: currentBizId,
          details: {
            dumper_instance_ids: [data.id],
          },
          remark: '',
          ticket_type: TicketTypes.TBINLOGDUMPER_REDUCE_NODES,
        };
        runCreateTicket(params);
      },
      type: 'warning',
      width: 400,
    });
  };

  // 批量删除
  const handleBatchDeleteInstance = () => {
    InfoBox({
      confirmButtonTheme: 'danger',
      confirmText: t('删除'),
      content: t('删除后数据传输将会终止，并删除实例，请谨慎操作！'),
      onConfirm: () => {
        const params = {
          bk_biz_id: currentBizId,
          details: {
            dumper_instance_ids: selectedList.value.map((item) => item.id),
          },
          remark: '',
          ticket_type: TicketTypes.TBINLOGDUMPER_REDUCE_NODES,
        };
        runCreateTicket(params);
      },
      title: t('确认批量删除n个实例？', { n: selectedList.value.length }),
      type: 'warning',
      width: 400,
    });
  };

  const handleOpenManualMigration = (data: DumperInstanceModel) => {
    if (data.isOperating) {
      return;
    }
    activeRow.value = data;
    showManualMigration.value = true;
  };

  const handleTableRequestFinished = (data: any[]) => {
    tableDataCount.value = data.length;
  };

  // 设置行样式
  const setRowClass = (row: DumperInstanceModel) => {
    const rowClasses: string[] = [];
    if (row.isNew) {
      rowClasses.push('is-new-row');
    }
    if (!row.isOnline) {
      rowClasses.push('is-stoped');
    }
    return rowClasses.join(' ');
  };

  const handleClearFilters = () => {
    search.value = [];
    fetchTableData();
  };

  const handleCopyAll = (isInstance = false) => {
    const list = (tableRef.value.getData() as DumperInstanceModel[]).map((item) => `${item.ip}:${item.listen_port}`);
    if (!isInstance) {
      copy(list.map((inst) => inst.split(':')[0]));
      return;
    }
    copy(list);
  };

  const handleCopySelected = (isInstance = false) => {
    const list = selectedList.value.map((item) => `${item.ip}:${item.listen_port}`);
    if (!isInstance) {
      copy(list.map((inst) => inst.split(':')[0]));
      return;
    }

    copy(list);
  };

  const copy = (value: string[]) => {
    execCopy(value.join(','), t('复制成功，共n条', { n: value.length }));
  };

  // 选择单台
  const handleSelect = (_idList: string[], list: DumperInstanceModel[]) => {
    selectedList.value = list;
  };

  const handleColumnFilter = (data: {
    checked: string[];
    column: {
      field: string;
      label: string;
    };
    index: number;
  }) => {
    if (data.checked.length === 0) {
      search.value = search.value.filter((item) => item.id !== data.column.field);
      return;
    }
    const isAddType = data.column.field === 'add_type';
    search.value = [
      {
        id: data.column.field,
        name: data.column.label,
        values: data.checked.map((item) => ({
          id: item,
          name: isAddType ? syncTypeMap[item] : item,
        })),
      },
    ];
  };
</script>

