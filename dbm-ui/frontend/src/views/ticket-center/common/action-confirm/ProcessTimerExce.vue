<template>
  <DbPopconfirm
    :confirm-handler="handleApproval"
    placement="bottom"
    :title="t('单据执行确认')"
    trigger="click"
    :width="300">
    <slot />
    <template #content>
      <div>
        {{ t('操作：') }}
        <BkTag
          class="mr-4"
          theme="success"
          type="stroke">
          {{ t('确认执行') }}
        </BkTag>
        <span>{{ t('确定后，单据将跳过定时步骤立即进入下一步骤') }}</span>
      </div>
    </template>
  </DbPopconfirm>
</template>
<script setup lang="ts">
  import { ref } from 'vue';
  import { useI18n } from 'vue-i18n';

  import FlowMode from '@services/model/ticket/flow';
  import TicketModel from '@services/model/ticket/ticket';
  import { batchProcessTicket, batchProcessTodo } from '@services/source/ticketFlow';

  import { useEventBus } from '@hooks';

  import { messageSuccess } from '@utils';

  interface Props {
    data?: TicketModel<unknown>;
    todoData?: FlowMode<unknown>['todos'][number];
  }

  const props = defineProps<Props>();

  const { t } = useI18n();

  const eventBus = useEventBus();

  const isSubmitting = ref(false);

  const handleApproval = () => {
    isSubmitting.value = true;

    return Promise.resolve()
      .then(() => {
        if (props.data) {
          return batchProcessTicket({
            action: 'SKIP',
            ticket_ids: [props.data.id],
          });
        }
        if (props.todoData) {
          return batchProcessTodo({
            action: 'SKIP',
            operations: [
              {
                todo_id: props.todoData.id,
              },
            ],
          });
        }
        return Promise.reject();
      })
      .then(() => {
        messageSuccess(t('操作成功'));
        eventBus.emit('refreshTicketStatus');
      })
      .finally(() => {
        isSubmitting.value = false;
      });
  };
</script>
